/*program which will print all the numbers which are either between 50 and 70 or less than 20,or more than 90.-vaibhav bhaskar,11912060,IT*/
#include<stdio.h>
#include<math.h>
int main()
{
	int i;
	for(i=0;i<=20;i++)
	{
		printf("%d\n",i);
	}
	for(i=50;i<=70;i++)
	{
		printf("%d\n",i);
	}
	for(i=90;i<=100;i++)
	{
		printf("%d\n",i);
	}
}

